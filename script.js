//  project cards
function updateProjectCards(t) {
  const projectCards = document.querySelectorAll('.project-card');
  if (projectCards.length >= 3) {
    const titles = projectCards[0].querySelector('h3');
    const titles2 = projectCards[1].querySelector('h3');
    const titles3 = projectCards[2].querySelector('h3');
    const techs = projectCards[0].querySelector('.project-tech');
    const techs2 = projectCards[1].querySelector('.project-tech');
    const techs3 = projectCards[2].querySelector('.project-tech');
    
    if (titles) titles.textContent = t.project1;
    if (titles2) titles2.textContent = t.project2;
    if (titles3) titles3.textContent = t.project3;
    if (techs) techs.innerHTML = `🛠️ ${t.project1Tech}`;
    if (techs2) techs2.innerHTML = `🛠️ ${t.project2Tech}`;
    if (techs3) techs3.innerHTML = `🛠️ ${t.project3Tech}`;
  }
  
  //  timeline projects on about page
  const timelineItems = document.querySelectorAll('.timeline-item');
  if (timelineItems.length >= 7) {
    const proj1Title = timelineItems[4]?.querySelector('h3');
    const proj2Title = timelineItems[5]?.querySelector('h3');
    const proj3Title = timelineItems[6]?.querySelector('h3');
    const proj1Desc = timelineItems[4]?.querySelector('p');
    const proj2Desc = timelineItems[5]?.querySelector('p');
    const proj3Desc = timelineItems[6]?.querySelector('p');
    
    if (proj1Title) proj1Title.textContent = t.project1;
    if (proj2Title) proj2Title.textContent = t.project2;
    if (proj3Title) proj3Title.textContent = t.project3;
    if (proj1Desc) proj1Desc.textContent = t.project1Desc || t.project1Tech;
    if (proj2Desc) proj2Desc.textContent = t.project2Desc || t.project2Tech;
    if (proj3Desc) proj3Desc.textContent = t.project3Desc || t.project3Tech;
  }
}

